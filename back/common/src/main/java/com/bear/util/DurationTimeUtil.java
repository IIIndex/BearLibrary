package com.bear.util;

/**
 * 时间间隔
 *
 * @author moyulingjiu
 */
public class DurationTimeUtil {
    /**
     * 毫秒
     */
    public static final long MILLISECOND = 1;

    /**
     * 秒
     */
    public static final long SECOND = 1000 * MILLISECOND;

    /**
     * 五秒
     */
    public static final long FIVE_SECOND = 5 * SECOND;

    /**
     * 半分钟
     */
    public static final long HALF_MINUTE = 30 * SECOND;

    /**
     * 分钟
     */
    public static final long MINUTE = 60 * SECOND;

    /**
     * 十分钟
     */
    public static final long TEN_MINUTE = 10 * MINUTE;

    /**
     * 小时
     */
    public static final long HOUR = 60 * MINUTE;

    /**
     * 两个小时
     */
    public static final long TWO_HOUR = 2 * HOUR;

    /**
     * 天
     */
    public static final long DAY = 24 * HOUR;

    /**
     * 星期
     */
    public static final long WEEK = 7 * DAY;

    /**
     * 月
     */
    public static final long MONTH = 30 * DAY;
}
